const GEOGRAPHY_AICHI_PUBLIC_SECTION_META = Object.fromEntries(
  GEOGRAPHY_AICHI_PUBLIC_GROUPS.flatMap(group =>
    group.sections.map(section => [section.id, { chapterId: group.id, category: section.title }])
  )
);

const GEOGRAPHY_AICHI_PUBLIC_ITEMS = Object.entries(GEOGRAPHY_AICHI_PUBLIC_BANK).flatMap(([sectionId, rows]) =>
  rows.map(([question, answer, explanation], index) => {
    const meta = GEOGRAPHY_AICHI_PUBLIC_SECTION_META[sectionId];
    return {
      id: `gap-${sectionId}-${String(index + 1).padStart(2, "0")}`,
      english: question,
      japanese: answer,
      category: meta.category,
      chapterId: meta.chapterId,
      sectionId,
      tier: sectionId.startsWith("gap-exam-") ? "advanced" : "foundation",
      example: explanation,
      exampleJapanese: "",
    };
  })
);
